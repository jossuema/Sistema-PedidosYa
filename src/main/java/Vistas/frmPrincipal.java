    public static void mostrarMain(String ob){
        try{
            CardLayout cl = (CardLayout)(panelCentral.getLayout());
            cl.show(panelCentral, "Fondo");
        }catch(Exception ex){}
        panelCentral.repaint();
        if(ob.equals("Cliente")){
            jTextField1.setText(cliente.Nombre+" "+cliente.Apellido);
        }
        if(ob.equals("Repartidor")){
            jTextField2.setText(repartidor.Nombre+" "+repartidor.Apellido);
        }
        if(ob.equals("Transporte")){
            jTextField3.setText(transporte.Placa);
        }
        if(ob.equals("Pedido")){
            jTextField4.setText(pedido.Codigo_pedido);
            lbMensaje.setText("Pedido ordenado correctamente");
        }
        
    }
    
